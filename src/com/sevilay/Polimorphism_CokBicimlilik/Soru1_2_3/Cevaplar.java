package com.sevilay.Polimorphism_CokBicimlilik.Soru1_2_3;

public class Cevaplar {

	/**
	 * Kod örneklerim paketin içindedir.
	 * 
	 * 
	 * 4- Java Polimorphism (Çok Biçimlilik)
	 * 
	 * 
	 * Soru 1: Java'da çok biçimliliğin (polimorphism) ne olduğunu açıklayın ve bir
	 * örnek ile bu kavramın nasıl kullanılacağını gösterin.
	 * 
	 * Cevap 1: 
	 * Nesneye yönelik programlamanın önemli özelliklerinden biri de çok biçimliliktir. 
	 * --> Çok biçimlilik, bir sınıf içinde aynı isimde fakat farklı içerikte metotlar kullanabilme özelliğidir. 
	 * --> Çok biçimlilik, aynı metodun değişik sınıflar içinde yer alabilme imkanıdır. 
	 * Kısacası, 
	 * --> Bir nesne referansının başka bir sınıfa atanıyor olmasına Polimorphsim denir. 
	 * Yanii,
	 * Aga'nın tek bir emir verip birden fazla işi yaptırıyor olması POLİMORPHİSM'dir.
	 * 
	 * 
	 * 
	 * 
	 * Soru 2: Bir interface'in, çok biçimlilikle nasıl kullanılabileceğini
	 * açıklayın ve bir örnek verin.
	 * 
	 * Cevap 2: 
	 * Bir interface, farklı sınıfların benzer davranışları paylaşmasını sağlayan bir yapıdır. 
	 * Polimorphism ise, aynı isme sahip ancak farklı sınıfların bu ismi farklı şekillerde uygulaması durumudur. 
	 * Bu sayede farklı sınıfların aynı işlevi farklı şekillerde gerçekleştirebilmesi sağlanır.
	 * 
	 * 
	 * 
	 * 
	 * Soru 3: Dinamik ve statik çok biçimlilik arasındaki farkı açıklayın. Her
	 * ikisi için de örnekler verin.
	 * 
	 * Cevap 3:
	 * --> Dinamik çok biçimlilik, alt sınıfların üst sınıfların metotlarını ezmesi (override) 
	 * ve aynı arayüze sahip farklı sınıfların nesnelerinin aynı metodu farklı şekilde uygulaması anlamına gelir. 
	 ** Bu genellikle kalıtım (inheritance) ve sanal (virtual) metotlar ile sağlanır. 
	 ** Çağrıldığında hangi metotun çalıştırılacağı, nesnenin türüne göre çalışma zamanında belirlenir.
	 * --> Statik çok biçimlilik, aynı isimli ancak farklı parametre listelerine sahip metotları ifade eder. 
	 ** Java'da bu, aşırı yükleme (overloading) ile gerçekleştirilir. 
	 ** Yani, bir sınıfta aynı isme sahip farklı sayıda veya türde parametreler alan metotlar tanımlayabiliriz. 
	 ** Derleme zamanında hangi metotun çağrılacağına karar verilir.
	 * 
	 */

}
